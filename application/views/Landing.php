            <h2 class="mt40">Aturan main di lelang</h2>
            <p>Harga barang di lelang sering kali begitu menggiurkan. Namun, jika berminat mengikutinya, kita tetap mesti berhati-hati.</p>

            <p>Dengan begitu, misi kita mencari barang dengan harga miring lewat lelang bisa tercapai. Berikut ini tips ikut lelang khususnya bagi pemula:</p>
            <ol class="aman">
                <li>
                    <h3>Patok budget</h3>
                    <p>Saat lelang, bukan mustahil terjadi adu tawar yang sengit. Akhirnya, harga malah melambung tinggi. Kita mesti berpikir dingin saat ikut lelang.</p>
                    <p>Untuk menghindari kebablasan menawar, sebaiknya patok budget. Misalnya, mau beli mobil merek Toyota Avanza dengan budget maksimal Rp 100 juta. Jika harga yang ditawarkan seiring berjalannya lelang melebihi budget, tidak usah memaksakan diri untuk terus melakukan penawaran.</p>
                </li>
                <li>
                    <h3>Cek barang sebelum lelang dilaksanakan</h3>
                    <p>Beberapa hari sebelum lelang biasanya akan di lakukan open house. Karena barang/unitnya sangat bervariasi, Anda wajib melihat dan mengecek kondisi fisik, mesin, serta dokumen kendaraan yang dilelang</p>
                    <p>Pada kesempatan ini Anda pun bisa bertanya kepada petugas yang ada atau bisa juga membawa rekan yang mengerti untuk membantu melakukan pengecekan.</p>
                </li>
                <li>
                    <h3>Cari info harga pasar</h3>
                    <p>Karena misinya dapat barang dengan harga lebih murah, kita mesti membekali diri dengan informasi harga pasar barang yang jadi incaran itu. Informasi ini dipakai untuk mematok budget.</p>
                </li>
                <li>
                    <h3>Pastikan memilih balai lelang yang terpercaya</h3>
                    <p>Pilihlah penyelenggara lelang yang terpercaya. Pastikan balai lelang tersebut memiliki kredibilitas yang baik. Selain itu, pelajari secara menyeluruh tentang tata cara mengikuti lelang yang dilakukan penyelenggara tersebut.</p>
                </li>
            </ol>
        </div>
