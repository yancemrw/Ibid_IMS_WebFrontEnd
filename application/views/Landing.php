            <h2>Lelang untuk Semua</h2>
            <p>Lelang IBID terbuka untuk semua orang. Siapapun bisa mendaftar jadi peserta lelang dan melakukan penawaran (bid) untuk kendaraan atau gadget yang akan dilelang di IBID.</p>
            <p>Tidak perlu menjadi bagian dari perusahaan atau badan tertentu untuk bisa mengikuti lelang. Setiap individu dapat mengikuti semua lelang IBID, baik lelang onsite, lelang online, maupun Live Auction.</p>
            <p>Informasi mengenai kendaraan dan gadget yang akan dilelang pun terbuka untuk umum dan dapat diakses siapa saja. Peserta lelang juga bebas memilih jadwal lelang dan melakukan penawaran. Jadi, setiap orang punya kesempatan yang sama untuk memenangkan lelang. </p>
