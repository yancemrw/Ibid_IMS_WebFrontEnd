        <div class="mt40">
            <h2>Apa Itu lelang</h2>
            <p>Lelang adalah penjualan barang secara terbuka yang diselenggarakan oleh balai lelang dan dipimpin oleh pejabat lelang. Mekanismenya, setiap calon pembeli berlomba-lomba untuk melakukan penawaran harga lebih tinggi secara bertahap. Penawar <i>(bidder)</i> paling tinggi lah yang nantinya akan memenangkan lelang.</p>
            <p>
            <p>Dalam proses lelang, Balai lelang hanya menjadi penyelenggara lelang resmi yang membantu individu/ lembaga untuk menjual aset (objek lelang). Artinya, objek lelang tersebut bukan milik balai lelang, melainkan milik penjual dari individu/ lembaga (penitip) yang menitipkan asetnya kepada balai lelang untuk dilelang.</p>
            <p>Untuk “barang” lelang sendiri adalah segala sesuatu yang bisa dijual atau hak yang dapat dijual, contohnya tanah, bangunan,  kendaraan (baik mobil ataupun motor), alat berat, gadget, mesin dan lain sebagainya.</p>
            <p>Untuk mengikuti lelang, calon peserta lelang wajib menyetor uang jaminan, yaitu sejumlah uang yang disetor ke balai lelang sebelum pelaksanaan lelang sebagai syarat utama menjadi peserta lelang.</p>
            <p>Uang jaminan tersebut akan ditukarkan dengan Nomor Peserta Lelang (NPL) sebagai alat yang digunakan untuk melakukan penawaran saat lelang. Setelah lelang selesai, uang jaminan ini akan dikembalikan jika peserta tidak mendapatkan barang yang diinginkan atau tidak memenangkan lelang. Sebaliknya, jika peserta memenangkan lelang, uang jaminan tersebut akan mengurangi total harga barang yang harus dilunasi.</p>
        </div>
