			<h2>Siapa Kami ?</h2>
			<p>IBID-Balai Lelang Serasi (IBID) adalah balai lelang yang melelang produk-produk otomotif seperti kendaraan roda dua dan roda empat, namun saat ini kami juga telah memulai pelelangan untuk gadget dan alat berat. Berdiri sejak tahun 2007, IBID merupakan anak perusahaan PT.Serasi Auto Raya yang bernaung dibawah bendera Grup Astra.</p>
			<p>Siapapun dapat berpartisipasi menjadi peserta dalam lelang, baik perusahaan ataupun perorangan, dari pembelian skala besar hingga pembelian secara satuan.</p>
			<p>IBID selalu mengadakan lelang rutin setiap minggu di lebih dari 10 kota besar, dan hingga saat ini sudah mencapai lebih dari 30 kota lelang di Indonesia. Hingga akhir 2016, sekitar 27.000 kendaraan roda empat telah terjual melalui lebih dari 500 lelang setiap tahunnya. </p>
			<p>Anda tidak hanya dapat berpartisipasi dalam lelang untuk membeli kendaraan, namun IBID juga menjadi solusi channel penjualan untuk mitra perusahaan yang ingin menjual aset kendaraan mereka dalam skala besar. Tidak hanya untuk perusahaan, sebagai perorangan Anda juga dapat menitipkan kendaraan untuk dijual dalam lelang.</p>
